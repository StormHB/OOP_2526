package pckg_ide_2;

public class Zadatak_2_2 {

    public static void main(String[] args) {

        int a = 41;
        int b = 4;

        System.out.println("Operacije nad int tipom:");
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));

        float c = -49.0f;

        System.out.println("\nOperacije nad float tipom:");
        System.out.println("Korijen od |x| = " + Math.sqrt(Math.abs(c)));
        System.out.println("Apsolutna vrijednost od x = " + Math.abs(c));

    }
}
